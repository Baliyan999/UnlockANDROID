@Composable
private fun DocumentViewerDialog(
    doc: TermsDocument,
    isAgreed: Boolean,
    onAgreeToggle: (Boolean) -> Unit,
    onDismiss: () -> Unit,
    isReadOnly: Boolean = false,
) {
    val isDark = MaterialTheme.colorScheme.surface.luminance() < 0.5f
    val primaryText = MaterialTheme.colorScheme.onSurface
    val secondaryText = MaterialTheme.colorScheme.onSurfaceVariant
    val bgColor = if (isDark) Color(0xFF141929) else Color(0xFFF8F9FC)
    val cardBg = if (isDark) Color(0xFF1C2137) else Color.White
    val scrollState = rememberScrollState()

    // Track if user scrolled to bottom
    val hasReachedBottom by remember(scrollState.maxValue) {
        derivedStateOf {
            scrollState.maxValue == 0 || scrollState.value >= scrollState.maxValue - 50
        }
    }

    Dialog(
        onDismissRequest = onDismiss,
        properties = DialogProperties(usePlatformDefaultWidth = false),
    ) {
        Surface(
            modifier = Modifier
                .fillMaxSize()
                .statusBarsPadding(),
            color = bgColor,
        ) {
            Column(modifier = Modifier.fillMaxSize()) {
                // ── Header ──
                Surface(
                    color = cardBg,
                    shadowElevation = 2.dp,
                ) {
                    Row(
                        modifier = Modifier
                            .fillMaxWidth()
                            .padding(horizontal = 16.dp, vertical = 14.dp),
                        verticalAlignment = Alignment.CenterVertically,
                    ) {
                        Icon(
                            doc.icon,
                            contentDescription = null,
                            tint = doc.tint,
                            modifier = Modifier.size(24.dp),
                        )
                        Spacer(Modifier.width(12.dp))
                        Column(modifier = Modifier.weight(1f)) {
                            Text(
                                doc.title,
                                fontWeight = FontWeight.Bold,
                                fontSize = 17.sp,
                                color = primaryText,
                            )
                            Text(
                                doc.description,
                                fontSize = 12.sp,
                                color = secondaryText,
                            )
                        }
                        TextButton(onClick = onDismiss) {
                            Text(
                                "Закрыть",
                                color = doc.tint,
                                fontWeight = FontWeight.SemiBold,
                                fontSize = 15.sp,
                            )
                        }
                    }
                }

                // ── Document Content ──
                Box(
                    modifier = Modifier
                        .weight(1f)
                        .fillMaxWidth()
                        .verticalScroll(scrollState)
                        .padding(16.dp),
                ) {
                    Surface(
                        color = cardBg,
                        shape = RoundedCornerShape(16.dp),
                        shadowElevation = 1.dp,
                    ) {
                        Text(
                            doc.text,
                            fontSize = 14.sp,
                            lineHeight = 22.sp,
                            color = primaryText,
                            modifier = Modifier.padding(20.dp),
                        )
                    }
                }

                // ── Bottom Bar (only for acceptance mode) ──
                if (!isReadOnly) {
                    Surface(
                        color = cardBg,
                        shadowElevation = 4.dp,
                    ) {
                        Column(
                            modifier = Modifier
                                .fillMaxWidth()
                                .navigationBarsPadding()
                                .padding(horizontal = 16.dp, vertical = 12.dp),
                        ) {
                            Row(
                                modifier = Modifier
                                    .fillMaxWidth()
                                    .clip(RoundedCornerShape(14.dp))
                                    .clickable(enabled = hasReachedBottom) {
                                        onAgreeToggle(!isAgreed)
                                        if (!isAgreed) onDismiss()
                                    }
                                    .background(
                                        if (isAgreed) BrandGreen.copy(alpha = 0.12f)
                                        else if (hasReachedBottom) doc.tint.copy(alpha = 0.08f)
                                        else Color.Transparent,
                                        RoundedCornerShape(14.dp),
                                    )
                                    .padding(14.dp),
                                horizontalArrangement = Arrangement.spacedBy(10.dp),
                                verticalAlignment = Alignment.CenterVertically,
                            ) {
                                Icon(
                                    if (isAgreed) Icons.Default.CheckBox else Icons.Default.CheckBoxOutlineBlank,
                                    contentDescription = null,
                                    tint = if (isAgreed) BrandGreen else if (hasReachedBottom) primaryText else secondaryText.copy(alpha = 0.3f),
                                )
                                Text(
                                    "Ознакомлен(а) и согласен(на)",
                                    fontWeight = FontWeight.SemiBold,
                                    color = if (hasReachedBottom) primaryText else secondaryText.copy(alpha = 0.4f),
                                )
                            }
                            if (!hasReachedBottom) {
                                Spacer(Modifier.height(6.dp))
                                Text(
                                    "↓  Прокрутите документ до конца",
                                    fontSize = 11.sp,
                                    color = secondaryText,
                                    modifier = Modifier.align(Alignment.CenterHorizontally),
                                )
                            }
                        }
                    }
                }
            }
        }
    }
}

